// Função para adicionar os dados melhorados ao Firebase
export const addImprovedSampleDataToFirebase = async () => {
  const { addVadeMecumDocument } = await import("../../src/services/vadeMecumService")
  
  try {
    const results = []
    for (const document of sampleVadeMecumDataImproved) {
      const docData = {
        ...document,
        dataCriacao: new Date(),
        dataAtualizacao: new Date()
      }
      const result = await addVadeMecumDocument(docData)
      results.push(result)
    }
    
    const successCount = results.filter(r => r.success).length
    console.log(`${successCount} documentos melhorados adicionados com sucesso ao Vade Mecum`)
    
    return { success: true, count: successCount }
  } catch (error) {
    console.error('Erro ao adicionar dados melhorados:', error)
    return { success: false, error: error.message }
  }
}

// Função para testar a busca com diferentes termos
export const testSearchFunctionality = async () => {
  const { searchVadeMecumImproved } = await import('../services/vadeMecumServiceImproved')
  
  const testTerms = [
    'empregado',
    'salário',
    'férias',
    'jornada',
    'segurança',
    'artigo 58',
    'CLT',
    'contrato'
  ]
  
  console.log('=== TESTE DE FUNCIONALIDADE DE BUSCA ===')
  
  for (const term of testTerms) {
    try {
      const result = await searchVadeMecumImproved(term, 10)
      console.log(`\nBusca por "${term}":`)
      console.log(`- Encontrados: ${result.totalFound || result.documents.length} documentos`)
      
      if (result.documents.length > 0) {
        result.documents.forEach((doc, index) => {
          console.log(`  ${index + 1}. ${doc.titulo}`)
        })
      } else {
        console.log('  Nenhum documento encontrado')
      }
    } catch (error) {
      console.error(`Erro ao buscar "${term}":`, error)
    }
  }
  
  console.log('\n=== FIM DO TESTE ===')
}

